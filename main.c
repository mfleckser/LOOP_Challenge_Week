// HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1): reads the current value
// __HAL_TIM_SET_COUNTER(htim, 0): reset the counter
// HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0): read current pin value, hopefully it'll work?

void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) {
	static uint32_t prev_time, prev_tick = 0;
	static int is_first_value = 1, is_counting = 0, bit_index = 0, bits[5];

	if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1) {
		uint32_t curr_time = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);

		if (is_first_value) { // only for very first value read
			prev_time = curr_time;
			is_first_value = 0;
		} else {
			uint32_t dif;
			// reading the pin after a change, so negate the read value to get the
			// value of the previous bit group
			int group_val = !HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0);

			// find dif (delta_t * 5) and update prev_time
			// (dif * 5) ms since last edge
			if (curr_time > prev_time) {
				dif = curr_time - prev_time;
			} else {
				dif = (TIM2_PERIOD - prev_time) + curr_time;
			}

			dif = (dif - 1) / 2;

			//printf("dif = %d, val = %d, time = %d\n\r", dif, group_val, curr_time);

			prev_time = curr_time;

			if (!is_counting) { // watch for 25ms high state timing packet
				if (dif % 2 == 1 && group_val) { // odd length packet signifies timing packet
					is_counting = 1;
				}
			} else { // counting
				while (bit_index < 5 && dif > 0) {
					bits[bit_index] = group_val;
					bit_index++;
					dif -= 2;
				}

				if (bit_index == 5) {
					bit_index = 0;
					is_counting = dif > 0;
					printf("Num: %d\n\r", decode_int(bits));
				}

			}
		}
	}
}

int decode_int(int *bits) {
	return bits[1] + 2 * bits[2] + 4 * bits[3] + 8 * bits[4];
}
